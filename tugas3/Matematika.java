public class Matematika {
    public int pertambahan(int a, int b){
        int tambah = a + b;
        System.out.println("Hasil Pertambahan 12 + 12  = " + tambah);
        return tambah;
    }
    
    public int pengurangan(int a, int b){
        int kurang = a - b;
        System.out.println("Hasil Pengurangan 32 - 24  = " + kurang);
        return kurang;
    }
    
    public int perkalian(int a, int b){
        int kali = a * b;
        System.out.println("Hasil Perkalian 9 * 3      = " + kali);
        return kali;
    }
    
    public int pembagian(int a, int b){
        int bagi = a / b;
        System.out.println("Hasil Pembagian 24 / 2     = " + bagi);
        return bagi;
    }
}
